#include "minishell.h"

void	sig_handler(int sig)
{
	if (sig == SIGINT)
	{
		ft_putstr_fd("\n", 1);
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
	}
}

void	setup_signals()
{
	signal(SIGINT, sig_handler);
	signal(SIGQUIT,  SIG_IGN);
}
